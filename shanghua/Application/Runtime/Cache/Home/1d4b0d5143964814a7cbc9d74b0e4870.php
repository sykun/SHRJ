    <h1>给设计院里新人的建议</h1>
    <p>&nbsp;&nbsp; 按照民用建筑设计院和工业建筑设计院；民用设计院有私营的、也有政府的，按照资质分为：甲、乙、丙、丁四级，甲级最好，依此类推，区别是可以承接的工程类别不一样。有些工程只能甲级院做：比如国家大剧院，有些工程都可以做：比如2层的150平方米小商店。工业设计院一般是政府的，比如说轻工设计院、邮电设计院、冶金设计院、化工设计院等，同样是按照资质分类，一般分甲、乙2级，区别是可以承接的工程类别不一样。<br>
　　员工的工作成果将对设计院的整体效率直接产生巨大的影响。设计人员通常有三种工作状态，设计院需要根据不同的状态采取不同的应对措施，以实现个人效率的提升。对于有力无心的设计人员，需要采取短期的激励机制以促进其提升动力。然而员工的能力是有一定的局限性的，当员工的潜力完全被激发出来之后，激励机制对于员工绩效的提升促进力就不再显著，此时员工处于有心无力状态，需要对其进行能力上的培养。通过对其动力激励机制和能力培养机制，最终设计人员达到心力合一的状态，从而发挥出其最大的效率。
    </p>
