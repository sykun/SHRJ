    <h1>全国优秀工程勘察设计计算机软件奖申报</h1>
    <p>&nbsp;&nbsp; 各地方、各部门勘察设计同业协会，各直属分会及工作委员会，各会员单位：根据《全国工程勘察设计行业优秀工程勘察设计行业奖评选办法》(中设协字〔2015〕第23号，以下简称《评选办法》,现就开展2015年度优秀工程勘察设计行业奖(以下简称行业奖)评选工作通知如下：<br/><br/>
　　一、申报范围：<br/>
　　(一)综合类<br/>
　　1、工程勘察项目。<br/>
　　2、建筑工程项目。<br/>
　　3、住宅与住宅小区项目。<br/>
　　4、市政公用工程项目。<br/>
　　5、工程勘察设计计算机软件项目。<br/>
　　6、建筑工程标准设计项目。<br/><br/>
　　(二)专项类<br/>
　　1、岩土工程项目。<br/>
　　2、人防工程项目。<br/>
　　3、建筑结构专业项目。<br/>
　　4、园林景观设计项目。<br/>
　　5、建筑环境与设备专业项目。<br/>
　　获得民营企业分会“华彩奖”和传统建筑设计分会 “华筑奖”一等奖的项目可推荐参加“行业奖”建筑工程设计项目的评审;获得园林和景观设计分会“计成奖”一等奖的项目可推荐参加“行业奖”市政公用工程项目的评审。
　　行业奖评选办法和各专业申报细则(详见附件一)均可从中国勘察设计协会网站www.chinaeda.org下载。<br/><br/>
　　二、申报要求：<br/>
　　1、申报项目应符合《评选办法》要求，并于2014年3月31日前完成设计并通过竣工验收后运行使用一年以上。<br/>
　　2、申报单位应根据申请奖项类别填写申报表(详见附件一的各专业申报细则)，各类申报表的“申报单位联系人”应填写本单位负责评优工作的人员;申报表须由单位法定代表人签署意见，加盖单位公章。申报材料包括申报表、相关证明和附件(如工程设计图纸、照片和有关音像制品)等，须单独装订成册。寄送至所属地方、部门勘察设计同业协会。<br/>
　　三、申报材料：<br/>
　　1、申报表用A4格式纸张软皮装订一式一份和Word格式电子版一份;<br/>
　　2、证明与附件材料用A4格式纸张软皮装订一式一份和JPG格式电子版一份;<br/>
　　3、图纸用A3图幅软皮装订一式一份和JPG格式电子版一份。<br/>
　　上述申报材料在各专业申报细则中有具体要求的，以申报细则为准。<br/>
    </p>
