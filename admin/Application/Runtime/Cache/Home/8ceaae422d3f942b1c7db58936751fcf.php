    <h1>我公司荣获山东省十大软件企业创新奖</h1>
    <p>&nbsp;&nbsp; 本报讯 9月6日，2012中国软件产业发展暨企业创新高峰会在南京举行。大会发布了“2012中国十大创新软件企业和2012中国年度创新软件产品”的评选结果，中国航信继2010年之后第三次获得“中国十大创新软件企业”荣誉，“中国航信新一代机场旅客前端处理系统软件V.2.5”第三次获得“中国年度创新软件产品”奖项。<br>
　　“中国十大创新软件企业和中国年度创新软件产品”是由中国软件行业协会主办的年度评选活动，其目的是逐步建立以软件企业为主体、市场为导向、产学研相结合的技术创新体系，使企业真正成为研究开发投入的主体、技术创新活动的主体和创新成果应用的主体，全面提高软件产业的自主创新能力。评选从参选企业的软件技术创新、管理机制创新、研发投入创新、人才培养创新、自主品牌五个方面进行综合评价。<br>
　　近年来，中国航信的自主创新能力不断提高，软件著作权等自主知识产权数量有了明显增加，依靠创新形成了市场竞争优势，不但增强了自身抵御风险的能力，而且提高了可持续发展能力和核心竞争力。
    </p>
